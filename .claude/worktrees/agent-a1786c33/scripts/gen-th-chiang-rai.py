def write_attraction_file(fname, trans):
    src = os.path.join(BASE, fname)
    dst = os.path.join(OUT, fname)
    with open(src, 'r', encoding='utf-8') as f:
        data = json.load(f)
    data["title"] = trans["title"]
    data["meta_description"] = trans["meta_description"]
    data["intro"] = trans["intro"]
    if "local_wisdom" in trans:
        data["local_wisdom"] = trans["local_wisdom"]
    if "budget_hacks" in trans:
        data["budget_hacks"] = trans["budget_hacks"]
    if "cultural_notes" in trans:
        data["cultural_notes"] = trans["cultural_notes"]
    for i, item_t in enumerate(trans.get("items", [])):
        if i < len(data["items"]):
            for k in ["story","why_locals_love_it","insider_tips","price_reality","location_details","personal_moment"]:
                if k in item_t:
                    data["items"][i][k] = item_t[k]
    with open(dst, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    print(f"  Wrote {fname}")

